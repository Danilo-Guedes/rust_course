fn main() {

    //TYPE CASTING

    let ascii_code_of_plus = b'+'; // B REPRESENT BYTE LITERAL

    println!("ASCII code of + is: {}", ascii_code_of_plus); // the ascii code of + is 43

    let converted_ascii_code_of_plus = ascii_code_of_plus as char;

    println!("Converted ASCII code of 43 is: {}", converted_ascii_code_of_plus); // the converted ascii code of + is +


}
